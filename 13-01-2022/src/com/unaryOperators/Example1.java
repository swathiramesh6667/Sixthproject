package com.unaryOperators;

public class Example1 
{
	//Arithmetic Operators
	public static void main(String[] args) 
	{
		
 int a =40;
 int b =20;
 
System.out.println("+: "+(a+b));
System.out.println("-: "+(a-b));
System.out.println("*: "+(a*b));
System.out.println("/: "+(a/b));
System.out.println("%: "+(a%b));

	}

}
