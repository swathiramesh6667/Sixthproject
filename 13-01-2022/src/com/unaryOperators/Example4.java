package com.unaryOperators;

public class Example4 
{
	//Logical Operators:
		public static void main(String[] args)
	
	{
		
 int a =40;
 int b =20;
 boolean validate = (a >= b);//true

	System.out.println("&&: "+((a > b) && (a >= b)));//true
	
	System.out.println("||: "+((a < b) || (a >= b)));//true	
	
	System.out.println("!: "+ !validate);//false


	}

}
